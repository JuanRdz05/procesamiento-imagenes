async function lanzarTrivia(preguntas, colorPais) {
	let aciertos = 0;

	// 1. Mezclar aleatoriamente el arreglo de preguntas de forma segura (Fisher-Yates)
	const preguntasMezcladas = [...preguntas];
	for (let i = preguntasMezcladas.length - 1; i > 0; i--) {
		const j = Math.floor(Math.random() * (i + 1));
		[preguntasMezcladas[i], preguntasMezcladas[j]] = [
			preguntasMezcladas[j],
			preguntasMezcladas[i],
		];
	}

	// 2. Seleccionar solo las primeras 5
	const preguntasSeleccionadas = preguntasMezcladas.slice(0, 5);

	// Ahora iteramos sobre 'preguntasSeleccionadas' en lugar de 'preguntas'
	for (let i = 0; i < preguntasSeleccionadas.length; i++) {
		const opcionesObj = {};
		preguntasSeleccionadas[i].opciones.forEach(
			(opt) => (opcionesObj[opt] = opt),
		);

		const { value: respuestaUsuario } = await Swal.fire({
			title: `Pregunta ${i + 1} de ${preguntasSeleccionadas.length}`,
			text: preguntasSeleccionadas[i].q,
			input: "radio",
			inputOptions: opcionesObj,
			inputValidator: (value) => !value && "¡Debes elegir una opción!",
			confirmButtonText: "Responder",
			confirmButtonColor: colorPais,
			allowOutsideClick: false,
			customClass: {
				popup: "popup-trivia",
			},
		});

		// Verificación inmediata
		if (respuestaUsuario === preguntasSeleccionadas[i].a) {
			aciertos++;
			await Swal.fire({
				title: "¡Correcto! ✅",
				icon: "success",
				timer: 1200,
				showConfirmButton: false,
				customClass: {
					popup: "popup-feedback",
				},
			});
		} else {
			await Swal.fire({
				title: "Incorrecto ❌",
				text: `La respuesta correcta era: ${preguntasSeleccionadas[i].a}`,
				icon: "error",
				timer: 2000,
				showConfirmButton: false,
				customClass: {
					popup: "popup-feedback",
				},
			});
		}
	}

	// Resultado final basado en la nueva longitud (5)
	const mensajeFinal =
		aciertos === preguntasSeleccionadas.length
			? "¡Eres un experto! 🏆"
			: aciertos >= 3
				? "¡Buen trabajo! 👍"
				: "¡Sigue practicando! 📚";

	Swal.fire({
		title: mensajeFinal,
		text: `Has conseguido ${aciertos} de ${preguntasSeleccionadas.length} puntos.`,
		icon: aciertos >= 3 ? "success" : "warning",
		confirmButtonText: "Genial",
		confirmButtonColor: colorPais,
		customClass: {
			popup: "popup-final",
		},
	});
}

